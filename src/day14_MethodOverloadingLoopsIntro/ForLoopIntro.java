package day14_MethodOverloadingLoopsIntro;

public class ForLoopIntro {
    public static void main(String[] args) {

        System.out.println("Hello world!");
        System.out.println("Hello world!");
        System.out.println("Hello world!");
        System.out.println("Hello world!");

        System.out.println("--------------------");

        for (int i=1; i<10;i++){
            System.out.println(i + "."+ "Hos geldiniz!");
            /*
            1.Hos geldiniz!
            2.Hos geldiniz!
            3.Hos geldiniz!
            ....
            * */
        }
    }
}
